using System;

namespace _06._Reversed_Chars
{
    class Program
    {
        static void Main(string[] args)
        {
            char simbolA = char.Parse(Console.ReadLine());
            char simbolB = char.Parse(Console.ReadLine());
            char simbolC = char.Parse(Console.ReadLine());

            Console.Write(simbolC);
            Console.Write(" ");
            Console.Write(simbolB);
            Console.Write(" ");
            Console.Write(simbolA);
        }
    }
}
